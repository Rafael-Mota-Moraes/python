# Ambientes virtuais em Python (venv)
# Um ambiente virtual carreha toda a sua instalação do Python para a pasta no caminho escolhido.
# Ao ativar um ambiente virtual, a instalação do ambiente virtual será usada.
# venv é o modulo que vamos usar para criar ambientes virtuais.
# Você pode dar o nome que prefererir para um ambiente virtual, mas os mais comuns são:
# venv env .venv .env
