# conversão de tipos, coersão
# tupe convertion, typecasting, coercion...
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool

print('a' + 'b')
print('1', type('1'))
print(int('1') + 1)
print(float('1') + 1)  # retorna outro float
print(float('1.1') + 1.2)  # retorna outro float
