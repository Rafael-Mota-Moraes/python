# Tipo de dado bool (boolean)
# Ao qustionar algo em um programa só existem duas respostas possíveis (verdadeiro ou faso).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro

print(10 == 10)
print(10 != 10)
print(10 == 11)
